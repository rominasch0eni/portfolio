/* ═══════════════════════════════════════════
   I18N ENGINE
   ═══════════════════════════════════════════ */
window.currentLang = (function() {
  var p = new URLSearchParams(window.location.search).get('lang');
  if (p === 'en' || p === 'de') { localStorage.setItem('portfolioLang', p); return p; }
  return localStorage.getItem('portfolioLang') || 'de';
})();

function t(key) {
  var l = window.currentLang || 'de';
  var v = TRANSLATIONS[l] && TRANSLATIONS[l][key];
  if (v !== undefined) return v;
  return (TRANSLATIONS.de && TRANSLATIONS.de[key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var v = t(el.getAttribute('data-i18n'));
    if (v !== undefined) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var v = t(el.getAttribute('data-i18n-ph'));
    if (v !== undefined) el.placeholder = v;
  });
  document.querySelectorAll('.lang-btn').forEach(function(b) {
    b.classList.toggle('active', b.getAttribute('data-lang') === window.currentLang);
  });
  document.documentElement.lang = window.currentLang;
}

function setLang(lang) {
  window.currentLang = lang;
  localStorage.setItem('portfolioLang', lang);
  applyTranslations();
  if (typeof renderCards === 'function') renderCards();
  if (typeof renderAllProjectPages === 'function') renderAllProjectPages();
}

/* Project field helper: picks EN field when available */
function pl(p, key) {
  if (window.currentLang === 'en' && p.en && p.en[key] !== undefined) return p.en[key];
  return p[key];
}
function pln(p, f, k) {
  if (window.currentLang === 'en' && p.en && p.en[f] && p.en[f][k] !== undefined) return p.en[f][k];
  return p[f][k];
}
